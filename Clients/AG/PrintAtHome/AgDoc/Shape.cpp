CAGSymDrawing* CShape::GenerateSymbol(LPCTSTR strShapeName, RECT DestRect)
{	
	CAGSymDrawing* pSymDrawing = new CAGSymDrawing();
	if (!pSymDrawing)
		return NULL;

	pSymDrawing->SetDestRect(DestRect);
	pSymDrawing->SetShapeName(strShapeName);
	RegeneratePoints(pSymDrawing, true/*fAspect*/);
	return pSymDrawing;
}

